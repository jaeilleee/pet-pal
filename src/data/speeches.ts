/** 스탯 기반으로 적절한 대사 카테고리 결정 */
function getSpeechCategory(stats: PetStats): keyof SpeechSet {
  if (stats.hunger < 30) return 'hungry';
  if (stats.energy < 20) return 'tired';
  if (stats.happiness < 30) return 'bored';
  if (stats.happiness > 70) return 'happy';
  return 'happy';
}

/** 성격 + 스탯 기반 대사 반환 */
export function getPersonalitySpeech(personality: Personality, stats: PetStats): string {
  const category = getSpeechCategory(stats);
  const speeches = PET_SPEECHES[personality][category];
  return speeches[Math.floor(Math.random() * speeches.length)];
}

/** 특정 카테고리에서 성격 대사 반환 */
export function getSpeechFromCategory(
  personality: Personality,
  category: keyof SpeechSet,
): string {
  const speeches = PET_SPEECHES[personality][category];
  return speeches[Math.floor(Math.random() * speeches.length)];
}
